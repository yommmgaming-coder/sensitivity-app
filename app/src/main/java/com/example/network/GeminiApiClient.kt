    private fun getFallbackChatResponse(userMsg: String): String {
        val query = userMsg.lowercase()
        return when {
            query.contains("dpi") -> "For crisp one-taps without breaking your screen's touchscreen registers, increase your Android DPI in Developer Options by 40 to 80 points. Standard sweet spot: 480 DPI for medium screens, 640 DPI for heavy drag shooters! (Caution: too high DPI may reset your device)."
