package com.example.network

import android.util.Log
import com.example.BuildConfig
import com.squareup.moshi.JsonClass
import com.squareup.moshi.Moshi
import com.squareup.moshi.kotlin.reflect.KotlinJsonAdapterFactory
import okhttp3.OkHttpClient
import retrofit2.Retrofit
import retrofit2.converter.moshi.MoshiConverterFactory
import retrofit2.http.Body
import retrofit2.http.POST
import retrofit2.http.Query
import java.util.concurrent.TimeUnit

// --- Gemini REST API Request & Response Models ---

@JsonClass(generateAdapter = true)
data class Part(val text: String)

@JsonClass(generateAdapter = true)
data class Content(val parts: List<Part>, val role: String? = null)

@JsonClass(generateAdapter = true)
data class GenerationConfig(
    val temperature: Float? = null,
    val responseMimeType: String? = null
)

@JsonClass(generateAdapter = true)
data class GeminiRequest(
    val contents: List<Content>,
    val systemInstruction: Content? = null,
    val generationConfig: GenerationConfig? = null
)

@JsonClass(generateAdapter = true)
data class Candidate(val content: Content)

@JsonClass(generateAdapter = true)
data class GeminiResponse(val candidates: List<Candidate>?)

// --- Retrofit Endpoint Definition ---

interface GeminiApiService {
    @POST("v1beta/models/gemini-3.5-flash:generateContent")
    suspend fun generateContent(
        @Query("key") apiKey: String,
        @Body request: GeminiRequest
    ): GeminiResponse
}

// --- Singleton Client ---

object GeminiApiClient {
    private const val TAG = "GeminiApiClient"
    private const val BASE_URL = "https://generativelanguage.googleapis.com/"

    private val moshi = Moshi.Builder()
        .addLast(KotlinJsonAdapterFactory())
        .build()

    private val okHttpClient = OkHttpClient.Builder()
        .connectTimeout(60, TimeUnit.SECONDS)
        .readTimeout(60, TimeUnit.SECONDS)
        .writeTimeout(60, TimeUnit.SECONDS)
        .build()

    private val service: GeminiApiService by lazy {
        Retrofit.Builder()
            .baseUrl(BASE_URL)
            .client(okHttpClient)
            .addConverterFactory(MoshiConverterFactory.create(moshi))
            .build()
            .create(GeminiApiService::class.java)
    }

    /**
     * Executes content generation against the Gemini AI API.
     */
    suspend fun generateResponse(prompt: String, systemPrompt: String? = null): String {
        val apiKey = BuildConfig.GEMINI_API_KEY
        if (apiKey.isEmpty() || apiKey == "MY_GEMINI_API_KEY") {
            Log.e(TAG, "Gemini API Key is empty or placeholder! Please configure it in your Secrets panel in AI Studio.")
            return getFallbackResponse(prompt)
        }

        val request = GeminiRequest(
            contents = listOf(Content(parts = listOf(Part(prompt)))),
            systemInstruction = systemPrompt?.let { Content(parts = listOf(Part(it))) },
            generationConfig = GenerationConfig(temperature = 0.4f)
        )

        return try {
            val response = service.generateContent(apiKey, request)
            response.candidates?.firstOrNull()?.content?.parts?.firstOrNull()?.text
                ?: "No response content generated by AI."
        } catch (e: Exception) {
            Log.e(TAG, "Error calling Gemini API: ${e.localizedMessage}", e)
            "Error generated: ${e.localizedMessage}. Proposing offline calculation...\n" + getFallbackResponse(prompt)
        }
    }

    /**
     * Chat Assistant generator supporting multi-turn conversation context.
     */
    suspend fun generateChatResponse(chatHistory: List<com.example.data.ChatMessage>): String {
        val apiKey = BuildConfig.GEMINI_API_KEY
        if (apiKey.isEmpty() || apiKey == "MY_GEMINI_API_KEY") {
            return getFallbackChatResponse(chatHistory.lastOrNull()?.text ?: "")
        }

        // Convert db entity to Gemini history
        val geminiContents = chatHistory.takeLast(12).map { msg ->
            val role = if (msg.role == "user") "user" else "model"
            Content(parts = listOf(Part(msg.text)), role = role)
        }

        val systemPromptStr = """
            You are 'Sensitivity Pro AI', the ultimate expert virtual coach for Garena Free Fire. 
            Help players improve their sensitivity settings, custom HUDs, drag-headshot speeds, DPI configurations, and gameplay strategy. 
            Keep answers precise, gamers-focused, and motivating! Use gamer terminology where appropriate (such as 'drag shot', 'one-tap', 'gloo wall placement', 'DPI tuning').
        """.trimIndent()

        val request = GeminiRequest(
            contents = geminiContents,
            systemInstruction = Content(parts = listOf(Part(systemPromptStr))),
            generationConfig = GenerationConfig(temperature = 0.7f)
        )

        return try {
            val response = service.generateContent(apiKey, request)
            response.candidates?.firstOrNull()?.content?.parts?.firstOrNull()?.text
                ?: "Chat response generation failed. Please try again."
        } catch (e: Exception) {
            "Chat Coach: Service temporarily overloaded. Standard gameplay recommendation: Focus on General Sensitivity 95-100% and practice drag headshots with custom HUD sizes of 45% for smoother one-taps!"
        }
    }

